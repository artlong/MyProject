public class Card {
	private final String A;
	private final String B;
	private final String C;
	
	public Card(String A, String B, String C) {
		this.A = A;
		this.B = B;
		this.C = C;
	}

	public String getA() {
		return A;
	}

	public String getB() {
		return B;
	}

	public String getC() {
		return C;
	}
}
